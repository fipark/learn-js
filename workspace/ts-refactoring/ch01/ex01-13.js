/*
논리 연산자 &&, ||, !
*/

//논리 합(or)
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);
// 앞의 값이 참이면 앞의 값을 반환
console.log("hello" || "world");
console.log(90 || 30);
console.log(null || "world");
console.log(undefined || "world");
console.log(0 || 100);
